    private void botonigualActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_botonigualActionPerformed
        double segundoNumero;
        
        if(operacion.equals("nula")){
            etiquetanumero.setText(cadenaNumeros);
        }
        
        else if(operacion.equals("Sumar")){  //se elige la operacion que se va a realizar
            segundoNumero = Double.parseDouble(cadenaNumeros); //convierte el texton en numero
            resultado=primerNumero+segundoNumero;//realiza la operacion
            etiquetanumero.setText(String.format("%.2f", resultado));//coloca la condicion de como quedara la respesuta en este caso con 2 cifras decimales
            cadenaNumeros=String.valueOf(resultado);//convierte ese resultado otra ves en string para mostrar en la pantalla
            operacion="nula";//realiza la operacion nula para que vulva a estar como antes
            
        }
        
          else if(operacion.equals("restar")){  //se elige la operacion que se va a realizar
            segundoNumero = Double.parseDouble(cadenaNumeros); //convierte el texton en numero
            resultado=primerNumero-segundoNumero;//realiza la operacion
            etiquetanumero.setText(String.format("%.2f", resultado));//coloca la condicion de como quedara la respesuta en este caso con 2 cifras decimales
            cadenaNumeros=String.valueOf(resultado);//convierte ese resultado otra ves en string para mostrar en la pantalla
            operacion="nula";//realiza la operacion nula para que vulva a estar como antes
            
        }
          
         else if(operacion.equals("multiplicar")){  //se elige la operacion que se va a realizar
            segundoNumero = Double.parseDouble(cadenaNumeros); //convierte el texton en numero
            resultado=primerNumero*segundoNumero;//realiza la operacion
            etiquetanumero.setText(String.format("%.2f", resultado));//coloca la condicion de como quedara la respesuta en este caso con 2 cifras decimales
            cadenaNumeros=String.valueOf(resultado);//convierte ese resultado otra ves en string para mostrar en la pantalla
            operacion="nula";//realiza la operacion nula para que vulva a estar como antes
            
        }
           else if (operacion.equals("dividir")) {  //se elige la operacion que se va a realizar
            segundoNumero = Double.parseDouble(cadenaNumeros);//convierte el texton en numero
            if (segundoNumero == 0) {//genera la condicion de que si se divide por cero esto quedara infinito
                // etiquetanumero.setText("NoSeDividePor0"); //comentario que se mostrara en pantalla
                JOptionPane.showMessageDialog(this,"No se puede dividir entre 0");
            } else {
                resultado = primerNumero / segundoNumero; //operacion a realizar
                etiquetanumero.setText(String.format("% .2f", resultado));//coloca la condicion de como quedara la respesuta en este caso con 2 cifras decimales
                cadenaNumeros = String.valueOf(resultado);//convierte ese resultado otra ves en string para mostrar en la pantalla
                operacion = "nula";//realiza la operacion nula para que vulva a estar como antes
            }
        }
        
                       
        etiquetaMuestra.setText("");
        activado = true;
        punto = false;
    }//GEN-LAST:event_botonigualActionPerformed

